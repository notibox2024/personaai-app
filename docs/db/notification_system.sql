-- ============================================================
-- ENUM TYPES
-- ============================================================

CREATE TYPE notification.notification_type AS ENUM (
    'attendance', 'training', 'leave', 'overtime', 'payroll', 
    'meeting', 'deadline', 'birthday', 'holiday', 'general', 
    'system', 'urgent'
);

